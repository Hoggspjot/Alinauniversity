public class Unit1 {
    public static void main(String[] args) {

        byte a = 120;
        short b = 1200;
        int c = 12000;
        long d = 1_000_000L;

        float e = 1.2f;
        double t = 2.4;

        char ch = 'a';
        boolean bu = true;

        System.out.println("Имя a, тип byte, значение = " + a);
        System.out.println("Имя b, тип short, значение = " + b);
        System.out.println("Имя c, тип int, значение = " + c);
        System.out.println("Имя d, тип long, значение = " + d);
        System.out.println("Имя e, тип float, значение = " + e);
        System.out.println("Имя t, тип double, значение = " + t);
        System.out.println("Имя ch, тип char, значение = " + ch);
        System.out.println("Имя bu, тип boolean, значение = " + bu);
    }
}
